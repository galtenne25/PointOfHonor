// ── Haversine distance ────────────────────────────────────────────────────────
function haversineKm(lat1, lon1, lat2, lon2) {
  const R = 6371, toRad = d => (d * Math.PI) / 180
  const dLat = toRad(lat2 - lat1), dLon = toRad(lon2 - lon1)
  const a = Math.sin(dLat / 2) ** 2 +
            Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLon / 2) ** 2
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
}

function totalDistanceKm(sites) {
  let d = 0
  for (let i = 1; i < sites.length; i++)
    d += haversineKm(sites[i-1].latitude, sites[i-1].longitude, sites[i].latitude, sites[i].longitude)
  return Math.round(d * 10) / 10
}

// Heritage tours in Israel are driving routes with a walk at each site.
// Short routes (< 12 km) are genuine hikes; longer ones are driving + dwell.
function formatDurationHe(km, stops) {
  const min = km < 12
    ? Math.round((km / 3.5) * 60) + stops * 20   // hiking pace + 20 min/stop
    : Math.round((km / 45)  * 60) + stops * 35   // driving pace + 35 min/stop
  const h = Math.floor(min / 60), m = min % 60
  if (h === 0) return `${m} דקות`
  if (m === 0) return `${h} שעות`
  return `${h} שעות ו-${m} דקות`
}

