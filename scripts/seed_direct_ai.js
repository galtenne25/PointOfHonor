// ── Clean tables ──────────────────────────────────────────────────────────────
async function cleanTables(supabase) {
  console.log('🧹 מנקה טבלאות קיימות...')

  const { error: rpcErr } = await supabase.rpc('truncate_seed_tables')
  if (!rpcErr) {
    console.log('   ✓ נוקה דרך RPC\n')
    return
  }
  console.log(`   ⚠️  RPC לא זמין (${rpcErr.message}), נמחק ידנית...`)
  for (const tbl of ['route_waypoints', 'routes', 'memorial_sites']) {
    const { error } = await supabase.from(tbl).delete().gte('id', 0)
    if (error) console.log(`   ⚠️  לא ניתן למחוק ${tbl}: ${error.message}`)
  }
  console.log('   ✓ מחיקה ידנית הושלמה\n')
}

// ── Insert memorial sites ─────────────────────────────────────────────────────
async function insertMemorialSites(supabase) {
  console.log(`🏛️  מוסיף ${SITES.length} אתרי הנצחה...`)

  const rows = SITES.map((s, i) => ({
    name:               s.name,
    city:               s.city,
    latitude:           s.latitude,
    longitude:          s.longitude,
    category:           s.category,
    location_type:      s.location_type,
    unit_name:          s.unit_name,
    dedicated_to:       DEDICATIONS[i] ?? null,
    gregorian_date:     s.gregorian_date,
    hebrew_date:        s.hebrew_date,
    description_snippet: s.description_snippet,
    full_description:   s.full_description,
    cover_image_url:    null,
    wikidata_id:        null,
  }))

  const BATCH = 25
  let inserted = 0
  for (let i = 0; i < rows.length; i += BATCH) {
    const batch = rows.slice(i, i + BATCH)
    const { error } = await supabase.from('memorial_sites').insert(batch)
    if (error) {
      console.error(`   ✗ שגיאה בהוספת אתרים: ${error.message}`)
      process.exit(1)
    }
    inserted += batch.length
    process.stdout.write(`   ✓ נוספו ${inserted}/${rows.length}\r`)
  }
  console.log(`   ✅ ${SITES.length} אתרי הנצחה נוספו בהצלחה      `)
}

// ── Insert routes ─────────────────────────────────────────────────────────────
async function insertRoutes(supabase) {
  console.log(`\n🗺️  מחשב מסלולים ומוסיף ${ROUTES_CONFIG.length} מסלולים...`)

  const routeRows = ROUTES_CONFIG.map(r => {
    const sitesInRoute = r.siteIndices.map(i => SITES[i])
    const distKm       = totalDistanceKm(sitesInRoute)
    return {
      title:             r.title,
      description_short: r.description_short,
      difficulty:        r.difficulty,
      category:          r.category,
      start_location:    r.start_location,
      is_featured:       r.is_featured,
      distance_km:       distKm,
      duration:          formatDurationHe(distKm, sitesInRoute.length),
      cover_image_url:   null,
      map_image_url:     null,
    }
  })

  const { data: inserted, error } = await supabase
    .from('routes')
    .insert(routeRows)
    .select('id, title, distance_km, duration')

  if (error) {
    console.error(`   ✗ שגיאה בהוספת מסלולים: ${error.message}`)
    process.exit(1)
  }

  for (const r of inserted) {
    console.log(`   ✅ "${r.title}" — ${r.distance_km} ק"מ, ${r.duration}`)
  }

  return inserted
}

// ── Insert waypoints ──────────────────────────────────────────────────────────
async function insertWaypoints(supabase, insertedRoutes) {
  const allWaypoints = []
  for (let ri = 0; ri < ROUTES_CONFIG.length; ri++) {
    const routeId = insertedRoutes[ri].id
    ROUTES_CONFIG[ri].siteIndices.forEach((siteIdx, stopOrder) => {
      const s = SITES[siteIdx]
      allWaypoints.push({
        route_id:   routeId,
        stop_order: stopOrder + 1,
        name:       s.name,
        description: s.description_snippet,
        image_url:  null,
      })
    })
  }

  console.log(`\n📍 מוסיף ${allWaypoints.length} עצירות מסלול...`)

  const BATCH = 25
  let inserted = 0
  for (let i = 0; i < allWaypoints.length; i += BATCH) {
    const batch = allWaypoints.slice(i, i + BATCH)
    const { error } = await supabase.from('route_waypoints').insert(batch)
    if (error) {
      console.error(`   ✗ שגיאה: ${error.message}`)
    } else {
      inserted += batch.length
    }
  }
  console.log(`   ✅ ${inserted} עצירות נוספו`)
}

