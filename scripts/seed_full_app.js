// ── Resilient insert (auto-strips unknown cols, detects missing tables) ──────
const MISSING = Symbol('missing')
async function insertResilient(sb, table, rows, selectCols, opts = {}) {
  if (!rows.length) return { inserted: 0, data: [] }
  let work = rows
  for (let a = 0; a < 14; a++) {
    let q = opts.upsert ? sb.from(table).upsert(work, { onConflict: opts.onConflict })
                        : sb.from(table).insert(work)
    if (selectCols) q = q.select(selectCols)
    const { data, error } = await q
    if (!error) return { inserted: work.length, data: data ?? [] }
    const m = error.message || ''
    if (error.code === 'PGRST205' || /Could not find the table/i.test(m)) return { inserted: 0, status: MISSING }
    const cm = m.match(/Could not find the '([^']+)' column/i) || m.match(/column "([^"]+)" of relation/i)
    if (cm) { const bad = cm[1]; work = work.map(r => { const c = { ...r }; delete c[bad]; return c }); continue }
    return { inserted: 0, error: m }
  }
  return { inserted: 0, error: 'too many column-strip retries' }
}
async function batchInsert(sb, table, rows, selectCols, opts) {
  let inserted = 0, data = [], status
  for (let i = 0; i < rows.length; i += BATCH) {
    const r = await insertResilient(sb, table, rows.slice(i, i + BATCH), selectCols, opts)
    if (r.status === MISSING) { status = MISSING; break }
    if (r.error) return { inserted, data, error: r.error }
    inserted += r.inserted; if (r.data) data = data.concat(r.data)
    process.stdout.write('.')
  }
  process.stdout.write('\n')
  return { inserted, data, status }
}
async function clearTable(sb, t) {
  const { error } = await sb.from(t).delete().gte('created_at', '1970-01-01')
  return error ? error.message : null
}

