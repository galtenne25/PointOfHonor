function randDateISO([, , start, end]) {
  const a = new Date(start).getTime(), b = new Date(end).getTime()
  return new Date(a + rnd() * (b - a)).toISOString().slice(0, 10)
}
function backdatedISO(maxDays) {
  return new Date(Date.now() - rnd() * maxDays * 86_400_000).toISOString()
}
function haversineKm(a, b, c, d) {
  const R = 6371, t = x => x * Math.PI / 180
  const dLa = t(c - a), dLo = t(d - b)
  const h = Math.sin(dLa/2)**2 + Math.cos(t(a))*Math.cos(t(c))*Math.sin(dLo/2)**2
  return R * 2 * Math.atan2(Math.sqrt(h), Math.sqrt(1 - h))
}
function durationHe(km, stops) {
  const min = km < 12 ? Math.round(km/3.5*60)+stops*20 : Math.round(km/45*60)+stops*35
  const h = Math.floor(min/60), m = min%60
  return h === 0 ? `${m} דקות` : m === 0 ? `${h} שעות` : `${h} שעות ו-${m} דקות`
}

